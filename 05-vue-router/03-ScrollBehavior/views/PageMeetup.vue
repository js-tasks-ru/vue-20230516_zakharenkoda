<template>
  <div class="page-meetup">
    <template v-if="meetup">
      <MeetupCover :title="meetup.title" :image="meetup.image" />

      <UiContainer>
        <div class="meetup">
          <div class="meetup__content">
            <h2>{{ meetup.title }}</h2>
            <p>
              <RouterLink :to="{ name: 'meetup.description', params: { meetupId } }">Description</RouterLink>
            </p>
            <p>
              <RouterLink :to="{ name: 'meetup.agenda', params: { meetupId } }">Agenda</RouterLink>
            </p>
            <RouterView :meetup="meetup" />
          </div>
          <div class="meetup__aside">
            <!-- ... -->
          </div>
        </div>
      </UiContainer>
    </template>
  </div>
</template>

<script>
import UiContainer from '../components/UiContainer.vue';
import UiAlert from '../components/UiAlert.vue';
import MeetupCover from '../components/MeetupCover.vue';
import { fetchMeetup } from '../api.js';

export default {
  name: 'PageMeetup',

  components: { MeetupCover, UiAlert, UiContainer },

  beforeRouteEnter(to) {
    return fetchMeetup(to.params.meetupId)
      .then((meetup) => {
        return (vm) => {
          vm.setMeetup(meetup);
        };
      })
      .catch(() => ({ name: 'meetups' }));
  },

  beforeRouteUpdate(to, from) {
    if (to.params.meetupId !== from.params.meetupId) {
      this.meetup = null;
      return fetchMeetup(to.params.meetupId)
        .then((meetup) => {
          this.setMeetup(meetup);
        })
        .catch(() => ({ name: 'meetups' }));
    }
    return true;
  },

  props: {
    meetupId: {
      type: String,
      required: true,
    },
  },

  data() {
    return {
      meetup: null,
    };
  },

  methods: {
    setMeetup(meetup) {
      this.meetup = meetup;
    },
  },
};
</script>

